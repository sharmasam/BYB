package com.byb.gambler.models;

public class AcceptedUser {
	private String mBirthday;
	private String mFullName;
	private String mID;
	private String mLastSeen;
	private String mThumbNail;
	
	public String getBirthday() {
		return mBirthday;
	}
	public void setBirthday(String mBirthday) {
		this.mBirthday = mBirthday;
	}
	public String getFullName() {
		return mFullName;
	}
	public void setFullName(String mFullName) {
		this.mFullName = mFullName;
	}
	public String getID() {
		return mID;
	}
	public void setID(String mID) {
		this.mID = mID;
	}
	public String getLastSeen() {
		return mLastSeen;
	}
	public void setLastSeen(String mLastSeen) {
		this.mLastSeen = mLastSeen;
	}
	public String getThumbNail() {
		return mThumbNail;
	}
	public void setThumbNail(String mThumbNail) {
		this.mThumbNail = mThumbNail;
	}
}
