package com.byb.gambler.models;

public class LeagueList {
	
	private String mNumberOfMatch;
	private String mID;
	private String mSports;
	private String mName;
	
	public String getNumberOfMatch() {
		return mNumberOfMatch;
	}
	public void setNumberOfMatch(String mNumberOfMatch) {
		this.mNumberOfMatch = mNumberOfMatch;
	}
	public String getID() {
		return mID;
	}
	public void setID(String mID) {
		this.mID = mID;
	}
	public String getSports() {
		return mSports;
	}
	public void setSports(String mSports) {
		this.mSports = mSports;
	}
	public String getName() {
		return mName;
	}
	public void setName(String mName) {
		this.mName = mName;
	}
	
	


}
